
public class IntegerTypes {

	public static void main(String[] args) {
		System.out.println("Min value for a byte variable = " + Byte.MIN_VALUE);
		System.out.println("Max value for a byte variable = " + Byte.MAX_VALUE);
		System.out.println("------------------------------------------");
		
		System.out.println("Min value for a short variable = " + Short.MIN_VALUE);
		System.out.println("Max value for a short variable = " + Short.MAX_VALUE);
		System.out.println("------------------------------------------");
		
		System.out.println("Min value for a int variable = " + Integer.MIN_VALUE);
		System.out.println("Max value for a int variable = " + Integer.MAX_VALUE);
		System.out.println("------------------------------------------");
		
		System.out.println("Min value for a long variable = " + Long.MIN_VALUE);
		System.out.println("Max value for a long variable = " + Long.MAX_VALUE);
		System.out.println("------------------------------------------");
		
		System.out.println("No of bits for int variable = " + Integer.SIZE);
		System.out.println("No of bytes for int variable = " + Integer.BYTES);
		System.out.println("------------------------------------------");
	}

}
